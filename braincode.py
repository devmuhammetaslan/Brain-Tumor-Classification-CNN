import os
import tensorflow as tf
from tensorflow.keras.preprocessing.image import ImageDataGenerator

# Klasör yollarımızı belirliyoruz (İndirdiğimiz zip dosyasının içinden Training ve Testing klasörleri çıkıyor)
base_dir = '/content/brain_tumor_dataset'
train_dir = os.path.join(base_dir, 'Training')
test_dir = os.path.join(base_dir, 'Testing')

# Proje gereksinimlerine göre görüntüler 224x224 boyutuna getirilecek
IMG_WIDTH = 224
IMG_HEIGHT = 224
BATCH_SIZE = 32

# Eğitim verisi için Veri Artırma (Data Augmentation) ve Normalizasyon işlemleri
# Neden yapıyoruz?: Modelin ezberlemesini (overfitting) önlemek ve farklı açılardan/koşullardan
# gelen tümör görüntülerini tanıyabilmesini sağlamak için veriyi çeşitlendiriyoruz.
train_datagen = ImageDataGenerator(
    rescale=1./255,           # Normalizasyon: Piksel değerlerini 0-1 aralığına sıkıştırır
    rotation_range=20,        # Görüntüyü rastgele 20 derece döndürür
    width_shift_range=0.1,    # Rastgele yatay kaydırma
    height_shift_range=0.1,   # Rastgele dikey kaydırma
    horizontal_flip=True,     # Yatay eksende rastgele çevirme
    validation_split=0.2      # Eğitim verisinin %20'sini modelin kendini test etmesi için ayırıyoruz
)

# Test verisi için sadece Normalizasyon yapıyoruz

test_datagen = ImageDataGenerator(rescale=1./255)

print("Eğitim verileri hazırlanıyor...")
train_generator = train_datagen.flow_from_directory(
    train_dir,
    target_size=(IMG_WIDTH, IMG_HEIGHT),
    batch_size=BATCH_SIZE,
    class_mode='categorical', # Çok sınıflı yapı (Glioma, Meningioma, vs.)
    subset='training'
)

print("\nDoğrulama (Validation) verileri hazırlanıyor...")
validation_generator = train_datagen.flow_from_directory(
    train_dir,
    target_size=(IMG_WIDTH, IMG_HEIGHT),
    batch_size=BATCH_SIZE,
    class_mode='categorical',
    subset='validation'
)

print("\nTest verileri hazırlanıyor...")
test_generator = test_datagen.flow_from_directory(
    test_dir,
    target_size=(IMG_WIDTH, IMG_HEIGHT),
    batch_size=BATCH_SIZE,
    class_mode='categorical'
)

from tensorflow.keras.models import Sequential
from tensorflow.keras.layers import Conv2D, MaxPooling2D, Flatten, Dense, Dropout

# Sınıflandırma modelimi ardışık (Sequential) bir mimari üzerine kurdum.
model = Sequential()

# 1. Evrişim (Convolution) Bloğu - Özellik Çıkarımı (Feature Extraction)
# Görüntüdeki kenar, çizgi gibi temel özellikleri yakalamak için 32 filtreli Conv2D katmanı ekledim.
# Giriş boyutunu projenin ön işleme aşamasında belirlediğim 224x224x3 (RGB) olarak ayarladım.
model.add(Conv2D(32, (3, 3), activation='relu', input_shape=(IMG_WIDTH, IMG_HEIGHT, 3)))
# Boyut azaltarak işlem yükünü hafifletmek ve önemli özellikleri öne çıkarmak için MaxPooling uyguladım.
model.add(MaxPooling2D(pool_size=(2, 2)))

# 2. Evrişim Bloğu
# Daha karmaşık örüntüleri (tümörün dokusu, şekli) yakalamak için filtre sayısını 64'e çıkardım.
model.add(Conv2D(64, (3, 3), activation='relu'))
model.add(MaxPooling2D(pool_size=(2, 2)))

# 3. Evrişim Bloğu
# Daha da derin ve spesifik özellikleri öğrenmesi için 128 filtreli bir katman daha ekledim.
model.add(Conv2D(128, (3, 3), activation='relu'))
model.add(MaxPooling2D(pool_size=(2, 2)))

# Vektörleştirme (Flatten)
# Evrişim katmanlarından çıkan 2 boyutlu matrisleri, tam bağlantılı (Dense) katmanlara
# verebilmek için tek boyutlu bir diziye (vektöre) dönüştürdüm.
model.add(Flatten())

# Tam Bağlantılı (Fully Connected) Katmanlar
# Çıkarılan özellikleri birleştirip karar mekanizması oluşturmak için 128 nöronlu gizli bir katman ekledim.
model.add(Dense(128, activation='relu'))

# Modelin ezberlemesini (overfitting) engellemek için eğitim sırasında nöronların %50'sini rastgele kapattım (Dropout).
model.add(Dropout(0.5))

# Çıkış Katmanı
# Veri setimizde 4 sınıf (glioma, meningioma, pituitary, no_tumor) olduğu için 4 nöronlu çıkış katmanı kullandım.
# Çok sınıflı bir sınıflandırma problemi olduğu için olasılık dağılımı veren 'softmax' aktivasyonunu tercih ettim.
model.add(Dense(4, activation='softmax'))

# Modeli derleme aşaması
# Optimizasyon için genelde en iyi ve hızlı sonucu veren 'adam' optimizasyon algoritmasını seçtim.
# Kayıp fonksiyonu (loss) olarak, veri setim 4 sınıflı olduğu için 'categorical_crossentropy' kullandım.
# Başarıyı ölçmek değerlendirme metriği olarak Accuracy (doğruluk) metriğini takip edeceğim.
model.compile(optimizer='adam',
              loss='categorical_crossentropy',
              metrics=['accuracy'])

# Kurduğum mimarinin katmanlarını, parametre sayılarını ve çıkış boyutlarını görmek için modelin özetini yazdırdım.
model.summary()

# Modeli eğitmeye (training) başlıyorum. Eğitim sürecindeki doğruluk (accuracy) ve kayıp (loss)
# değerlerinin adım adım değişimini sonradan inceleyebilmek için tüm süreci 'history' adlı değişkene kaydettim.
history = model.fit(
    train_generator,
    epochs=10, # Modelin tüm veri setini 10 tam tur (epoch) görerek öğrenmesini sağladım.
    validation_data=validation_generator # Modelin ezber (overfitting) yapmadığından emin olmak için her tur sonunda doğrulama verisiyle test ettim.
)

# Eğitim bittikten sonra, proje tanımında benden istenen "Değerlendirme Metrikleri" bölümünü
# tamamlamak için modelin daha önce HİÇ GÖRMEDİĞİ test verileri (test_generator) üzerinde nihai bir ölçüm yaptım.
test_loss, test_accuracy = model.evaluate(test_generator)
print(f"\nModelin Test Verisindeki Doğruluğu (Accuracy): %{test_accuracy * 100:.2f}")

# Eğittiğim bu modeli daha sonra yeniden eğitmek zorunda kalmadan
# doğrudan kullanabilmek için dosyaya kaydettim.
model.save('beyin_tumoru_modeli.h5')

