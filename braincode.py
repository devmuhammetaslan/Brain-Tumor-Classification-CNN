import numpy as np
from tensorflow.keras.preprocessing import image
from tensorflow.keras.models import load_model
import os

# 1. Az önce eğittiğimiz ve kaydettiğimiz modeli yüklüyoruz
model_filename = 'gelismis_beyin_tumoru_modeli.h5'

if not os.path.exists(model_filename):
    print(f"HATA: Model dosyası '{model_filename}' bulunamadı.\nLütfen '2fH_dWXbfRZD' ID'li hücreyi (Transfer Learning ile model eğitim hücresi) çalıştırarak modeli yeniden eğitin ve kaydedin.")
else:
    model = load_model(model_filename)

    # Sınıf isimlerimizi alfabetik olarak tanımlıyoruz (Kaggle veri setindeki klasör sırasına göre)
    class_names = ['glioma', 'meningioma', 'no_tumor', 'pituitary']

    def tumor_tahmini_yap(resim_yolu):
        try:
            # Görüntüyü modelin beklediği 224x224 boyutunda yüklüyoruz
            img = image.load_img(resim_yolu, target_size=(224, 224))
        except FileNotFoundError:
            print(f"HATA: '{resim_yolu}' yolunda resim bulunamadı.")
            print("Lütfen sol taraftaki dosya yöneticisinden Testing klasörüne girip bir resmin yolunu kopyalayarak 'ornek_resim_yolu' değişkenini güncelle.")
            return

        # Görüntüyü matematiksel bir diziye (array) çevirip normalizasyon yapıyoruz
        img_array = image.img_to_array(img)
        img_array = np.expand_dims(img_array, axis=0) # Model resimleri "paket" halinde beklediği için boyut ekliyoruz
        img_array /= 255.0 # Piksel değerlerini 0-1 arasına çekiyoruz

        # Model tahmini yapıyor (hafızadaki 'model' değişkenini DOĞRUDAN kullanıyoruz)
        tahminler = model.predict(img_array, verbose=0)

        # Çıkan 4 farklı olasılıktan en yüksek olanı buluyoruz
        en_yuksek_ihtimal_indeksi = np.argmax(tahminler[0])
        tahmin_edilen_sinif = class_names[en_yuksek_ihtimal_indeksi]
        guven_orani = tahminler[0][en_yuksek_ihtimal_indeksi] * 100

        # Sonucu ekrana temiz bir şekilde yazdırıyoruz
        print("\n--- SİSTEM TAHMİN SONUCU ---")
        if tahmin_edilen_sinif == 'no_tumor':
            print(f"Bulgu: %{guven_orani:.2f} güven oranıyla TÜMÖR YOK (Sağlıklı).")
        else:
            print(f"Bulgu: %{guven_orani:.2f} güven oranıyla TÜMÖR TESPİT EDİLDİ.")
            print(f"Tümör Türü: {tahmin_edilen_sinif.capitalize()}")

    # Test klasörünün içinden rastgele bir Glioma tümörü resmi seçip sisteme veriyoruz:
    ornek_resim_yolu = '/content/brain_tumor_dataset/Testing/glioma/Te-gl_129.jpg'
    tumor_tahmini_yap(ornek_resim_yolu)

