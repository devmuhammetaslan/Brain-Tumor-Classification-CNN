from tensorflow.keras.applications import VGG16
from tensorflow.keras.models import Model
from tensorflow.keras.layers import Dense, Dropout, GlobalAveragePooling2D

# Doğruluk oranını artırmak ve daha profesyonel bir özellik çıkarımı (feature extraction)
# yapmak için Transfer Learning (Transfer Öğrenme) yöntemini tercih ettim.
# Daha önceden milyonlarca görüntüyle eğitilmiş olan güçlü VGG16 mimarisini temel aldım.
# include_top=False diyerek modelin orijinal sınıflandırma kısmını attım, sadece özellik çıkarma kısmını aldım.
base_model = VGG16(weights='imagenet', include_top=False, input_shape=(IMG_WIDTH, IMG_HEIGHT, 3))

# VGG16'nın önceden öğrenmiş olduğu mükemmel özellikleri bozmamak için
# temel katmanların ağırlıklarını dondurdum (eğitilmesini engelledim).
for layer in base_model.layers:
    layer.trainable = False

# VGG16'dan çıkan özellikleri kendi sınıflandırma yapıma bağlamak için yeni katmanlar ekledim.
x = base_model.output
# Flatten yerine daha modern ve performansı artıran GlobalAveragePooling2D kullandım.
x = GlobalAveragePooling2D()(x)
# Modeli kendi verime adapte etmek için 256 nöronlu güçlü bir tam bağlantılı katman (Dense) ekledim.
x = Dense(256, activation='relu')(x)
# Aşırı öğrenmeyi (overfitting) engellemek için %50'lik bir Dropout uyguladım.
x = Dropout(0.5)(x)

# Proje gereksinimi olan 4 sınıflı (Glioma, Meningioma, Pituitary tumor, Sağlıklı) yapı için çıkış katmanı.
predictions = Dense(4, activation='softmax')(x)

# VGG16'nın temeli ile kendi eklediğim sınıflandırma başlığını birleştirerek nihai modelimi oluşturdum.
advanced_model = Model(inputs=base_model.input, outputs=predictions)

# Yeni modelimi derliyorum
advanced_model.compile(optimizer='adam',
                       loss='categorical_crossentropy',
                       metrics=['accuracy'])

print("Transfer Learning (VGG16) ile yeni modelin eğitimi başlıyor...")

# Gelişmiş modelimi eğitiyorum. Sadece eklediğim son katmanlar eğitildiği için süreç oldukça hızlı ilerleyecektir.
advanced_history = advanced_model.fit(
    train_generator,
    epochs=10,
    validation_data=validation_generator
)

# Yeni modelin daha önce HİÇ GÖRMEDİĞİ test verisindeki başarısını ölçüyorum
advanced_test_loss, advanced_test_accuracy = advanced_model.evaluate(test_generator)
print(f"\nTransfer Learning Sonrası Yeni Test Doğruluğu: %{advanced_test_accuracy * 100:.2f}")

# Yüksek doğruluk oranına sahip bu yeni modeli kaydediyorum.
advanced_model.save('gelismis_beyin_tumoru_modeli.h5')

